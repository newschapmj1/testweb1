<br>
<h4>EASY ACCESS (EA)</h4>
<b>Suitable for:</b> 
<p>Everyone, including those in wheelchairs and pushchairs. Comfortable shoes or trainers can be worn.</p>
<b>Terrain:</b><br>
<p>Good wide paths with flat, smooth and relatively hard surfaces, absolute minimum of obstructions such as steps, all gates and barriers wheelchair-accessible (no stiles), minimal gradients except over extremely short distances, with possibly a few steeper slopes if assistance is available to push wheelchairs.</p>
<b>Pace:</b>
<p>Suitable for wheelchair users.</p>
<b>Facilities:</b>
<p>Meeting point has suitable car parking for people with wheelchairs, and/or accessible public transport and toilets. Walks above 2 miles must include an opportunity to leave the walk early.<p>
<br>
<h4>EASY (E)</h4>
<b>Suitable for:</b>
<p>Anyone unless they have a mobility difficulty, a specific health problem or are seriously unfit, and generally for pushchairs if they can be lift-ed over occasional obstructions. Comfortable shoes or trainers can be worn.</p>
<b>Terrain:</b>
<p>Mostly on easy paths with good surfaces. May have a few gentle climbs but no lengthy and sustained ascents. Minimal obstructions,
including stiles, and no long or difficult flights of steps.</p>
<b>Facilities:</b>
<p>Walks above 2 miles must include an opportunity to leave the walk early.</p>
<b>Pace:</b>
Slow.
<b>Length:</b><br>
<p>No longer than 5 miles. Longer walks should be graded as leisurely.</p>
<br>
<h4>LEISURELY (L)</h4>
<b>Suitable for:</b>
<p>For reasonably fit people with at least a little country walking experience. Walking boots and warm, waterproof clothing are
recommended.</p>
<b>Terrain:</b>
<p>Includes unsurfaced country paths that may be narrow, uneven, muddy or overgrown, and obstructions such as stiles, narrow bridges,
stepping-stones and steps. May be hilly, with a few ascents and descents. May also include walks that would otherwise be graded easy
access or easy but are longer than 5 miles.</p>
<b>Pace:</b>
<p>Slow or moderate pace; could include otherwise Easy (E) walks taken at a moderate pace.</p>
<b>Length:</b>
<p>No longer than 9 miles, longer walks should be graded as moderate.</p>
<br>
<h4>MODERATE (M)</h4>
<b>Suitable for:</b>
<p>People with country walking experience and a good level of fitness. Walking boots and warm, waterproof clothing are essential.</p>
<b>Terrain:</b>
<p>As leisurely walks, but with some sustained climbs and perhaps some moderate walking on open hillsides or moorland in the warmer months.</p>
<b>Pace:</b>
<p>Moderate pace; could include walks on leisurely terrain taken at a brisk pace.</p>
<b>Length:</b>
<p>No longer than 13 miles, longer walks should be graded as strenuous.</p>
<br>
<h4>STRENUOUS (S)</h4>
<b>Suitable for:</b>
<p>Experienced country walkers with above average fitness. Walking boots and warm, waterproof clothing are essential. People in doubt are advised to contact the organiser or leader in advance.</p>
<b>Terrain:</b>
<p>Rough country and mountains, a few long or lots of short ascents and descents on rough paths and across open country.</p>
<b>Pace:</b>
<p>Moderate or brisk pace; could include walks on moderate terrain taken at a brisk pace.</p>

