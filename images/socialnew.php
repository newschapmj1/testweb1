<h3><strong>Coach Assisted Walking Holiday 26th-30th July 2016</strong></h3>

<p>This year’s coach assisted walking holiday will be The <a href="https://www.ldwa.org.uk/ldp/members/show_path.php?path_name=Limestone+Way">Limestone Way</a>. This is an easy 46 mile route from 
